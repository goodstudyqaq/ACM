    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());

    int ans = 0;
    for (int i = 0; i < n; i++) {
        int val = a[i];
        int num = n - i;

        int tmp = min(num, val);
        ans = max(ans, tmp);
    }
    cout << ans << '\n';
