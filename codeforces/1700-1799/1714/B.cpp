void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    vector<bool> vis(n + 1);

    int now = n - 1;
    while (now >= 0 && vis[a[now]] == 0) {
        vis[a[now]] = 1;
        now--;
    }
    cout << now + 1 << endl;
}

