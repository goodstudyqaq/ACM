void solve() {
    int n, m;
    cin >> n >> m;
    set<pii> limit;
    for (int i = 0; i < m; i++) {
        int idx, x;
        cin >> idx >> x;
        limit.insert({idx, x});
    }
    auto dp = vect(Mint(0), n + 1, n + 1, 2);

    auto get_val = [&](int idx, int len, int suffix_val, int idx2) {
        int l = idx - len + 1;
        int r2 = idx - suffix_val;
        // [r2 + 1, idx] -> 1, 2, ... suffix_val
        if (idx2 >= r2 + 1) {
            return idx2 - r2;
        }
        // [l, idx - 1] -> suffix_val + 1, ... len
        return idx2 - l + suffix_val + 1;
    };
    debug(limit);

    auto check = [&](int idx, int len, int suffix_val) -> bool {
        int l = idx - len + 1;
        // [l, idx]

        auto itor1 = limit.lower_bound({l, -1});
        auto itor2 = limit.upper_bound({idx, 1e9});

        if (itor1 == limit.end() || itor1->first > idx) {
            return true;
        }

        while (itor1 != itor2) {
            int idx2 = itor1->first;
            int val = itor1->second;

            int val2 = get_val(idx, len, suffix_val, idx2);
            // debug(idx, len, suffix_val, idx2, val2);
            if (val == val2) {
                return false;
            }
            itor1++;
        }
        return true;
    };
    // dp[0][0][1] = 1;
    dp[0][0][0] = 1;

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= i; j++) {
            // i 结尾，长度为 j, k = 1 的话就是以 1, 2, 3, ... j 结尾， k = 0 就是其他方式
            Mint tmp = 0;
            for (int k = 0; k <= i - j; k++) {
                tmp += dp[i - j][k][0] + dp[i - j][k][1];
            }
            if (check(i, j, j)) {
                dp[i][j][1] += tmp;
            }

            for (int k = 1; k < j; k++) {
                if (check(i, j, k)) {
                    // if (i == 3 && j == 3) {
                    //     debug(k);
                    // }
                    dp[i][j][0] += tmp;
                    // 长度为 j，以 1, 2, .. k 结尾。那么这里需要减掉 dp[i - j][k][1]
                    dp[i][j][0] -= dp[i - j][k][1];
                }
            }
            // debug(i, j, dp[i][j]);
        }
    }
    Mint ans = 0;
    for (int i = 1; i <= n; i++) {
        ans += dp[n][i][0] + dp[n][i][1];
    }
    cout << ans << '\n';
}

