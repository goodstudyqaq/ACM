void solve() {
    int n, m;
    cin >> n >> m;

    vector<long long> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    reverse(a.begin(), a.end());

    int mi = min(n, m);
    long long now = m;
    for (int i = 0; i < mi; i++) {
        a[i] *= now;
        now--;
    }

    long long ans = 0;
    for (int i = 0; i < mi; i++) {
        ans += a[i];
    }
    cout << ans << '\n';
}

