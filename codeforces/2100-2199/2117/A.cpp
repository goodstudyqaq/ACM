void solve() {
    int n, x;
    cin >> n >> x;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    int beg = 0;
    while (beg < n && a[beg] == 0) beg++;

    int end = n - 1;
    while (end >= 0 && a[end] == 0) end--;

    int len = end - beg + 1;
    if (len > x) {
        cout << "NO\n";
    } else {
        cout << "YES\n";
    }
}

