int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
#ifdef LOCAL
    freopen("data.in", "r", stdin);
#else
#endif
    int n, m;
    cin >> n >> m;
    vector< vector< int > > V;
    vector< int > in;
    in.resize(n + 1);
    V.resize(n + 1);
    auto cal = [&](int idx) {
        int all = in[idx];
        int big = V[idx].size();
        return 1LL * (all - big) * big;
    };

    for (int i = 1; i <= m; i++) {
        int u, v;
        cin >> u >> v;
        if (u < v) swap(u, v);
        V[v].push_back(u);
        in[u]++;
        in[v]++;
    }

    long long res = 0;
    for (int i = 1; i <= n; i++) {
        res += cal(i);
    }
    cout << res << '\n';
    int q;
    cin >> q;
    while (q--) {
        int idx;
        cin >> idx;
        res -= cal(idx);
        for (auto it : V[idx]) {
            res -= cal(it);
            V[it].push_back(idx);
            res += cal(it);
        }
        V[idx].clear();
        cout << res << '\n';
    }
    return 0;
}
