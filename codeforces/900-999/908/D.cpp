const int mod = 1e9 + 7;
long long quick(long long a, long long b) {
	long long res = 1;
	while (b) {
		if (b % 2) res = res * a % mod;
		a = a * a % mod;
		b /= 2;
	}
	return res;
}
long long inv_pa, inv_pb, inv_pab;
int k, pa, pb;

vector< vector< long long > > dp;
long long dfs(int i, int j) {
	if (i + j >= k) {
		return ((1LL * pa * inv_pb % mod + i) % mod + j) % mod;
	}
	if (dp[i][j] != -1) return dp[i][j];

	long long res = (dfs(i + 1, j) * pa % mod * inv_pab % mod + dfs(i, j + i) * pb % mod * inv_pab % mod) % mod;
	return dp[i][j] = res;
}

