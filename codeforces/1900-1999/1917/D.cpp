constexpr int md = 998244353;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

vector<Mint> fact(1, 1);
vector<Mint> inv_fact(1, 1);

Mint C(int n, int k) {
    if (k < 0 || k > n) {
        return 0;
    }
    while ((int)fact.size() < n + 1) {
        fact.push_back(fact.back() * (int)fact.size());
        inv_fact.push_back(1 / fact.back());
    }
    return fact[n] * inv_fact[k] * inv_fact[n - k];
}

Mint A(int n, int k) {
    if (k < 0 || k > n) {
        return 0;
    }
    while ((int)fact.size() < n + 1) {
        fact.push_back(fact.back() * (int)fact.size());
        inv_fact.push_back(1 / fact.back());
    }
    return fact[n] * inv_fact[n - k];
}
template <typename T>
struct BIT {
#define lowbit(x) x & -x
    int n;
    vector<T> a;
    // [1, n]
    BIT(int n) : n(n), a(n + 1) {}
    BIT() {}

    void init(int _n) {
        n = _n;
        a = vector<T>(n + 1);
    }
    void add(int x, T v) {
        while (x <= n) {
            a[x] += v;
            x += lowbit(x);
        }
    }

    // [1, x]
    T query(int x) {
        T res = 0;
        while (x) {
            res += a[x];
            x -= lowbit(x);
        }
        return res;
    }

    static int largest_bit(int x) {
        return 31 - __builtin_clz(x);
    }

    int search(T val) {  // 找到最后一个 res，使得 [1, res] 的前缀和 <= val
        int level = largest_bit(n);
        int res = 0;
        for (int i = level; i >= 0; i--) {
            if (res + (1 << i) <= n && val >= a[res + (1 << i)]) {  // 注意 val >= a[res + (1 << i)], 如果求的是 < val，那么相应也要去掉 = 号
                res += (1 << i);
                val -= a[res];
            }
        }
        return res;
    }
};

void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> p(n), q(k);
    for (int i = 0; i < n; i++) {
        cin >> p[i];
    }
    for (int i = 0; i < k; i++) {
        cin >> q[i];
    }

    BIT<int> bit(k);
    // 求每个 block 里的逆序对

    long long q_inv = 0;
    for (int i = 0; i < k; i++) {
        int val = q[i] + 1;
        q_inv += i - bit.query(val);
        bit.add(val, 1);
    }

    debug(q_inv);

    Mint ans = Mint(q_inv) * Mint(n);
    debug(ans);

    // 求 block 间的逆序对

    bit.init(n * 2 - 1);

    for (int i = 0; i < n; i++) {
        debug(i);
        int val = p[i];

        int num = bit.query(val);
        int x = 1;
        long long div = 2;
        while (true) {
            if (num == 0) break;
            int num2 = bit.query(val / div);
            int num3 = num - num2;
            ans += Mint(1 + (k - x)) * Mint(k - x) / Mint(2) * Mint(num3);
            x++;
            if (x > k) break;
            div *= 2;
            num = num2;
        }

        num = bit.query(val);

        int mul = 2;
        x = 1;
        while (true) {
            if (num == i) break;
            int num2 = bit.query(min(val * mul, 2 * n - 1));
            // debug(num2, num);
            int num3 = num2 - num;
            if (x > k) {
                ans += Mint(num3) * Mint(k) * Mint(k);
            } else {
                ans += Mint(num3) * (Mint(x + k) * Mint(k - x + 1) / Mint(2) + Mint(x - 1) * Mint(k));
            }
            x++;
            mul *= 2;
            num = num2;
        }
        bit.add(val, 1);
        debug(ans);
    }
    cout << ans << endl;
}

