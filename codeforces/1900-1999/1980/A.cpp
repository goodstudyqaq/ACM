void solve() {
    int n, m;
    cin >> n >> m;
    string s;
    cin >> s;

    int ans = 0;
    vector<int> M(7);
    for (int i = 0; i < n; i++) {
        M[s[i] - 'A']++;
    }

    for (int i = 0; i < 7; i++) {
        ans += max(0, m - M[i]);
    }
    cout << ans << endl;
}

