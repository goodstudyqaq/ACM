void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    vector<int> ans(n);
    int mi = n;

    for (int i = n - 1; i >= 0; i--) {
        // mex = mi - pi = ai
        // pi = mi - ai
        ans[i] = mi - a[i];
        mi = min(mi, ans[i]);
    }
    for (int i = 0; i < n; i++) {
        cout << ans[i] << ' ';
    }
    cout << endl;
}

