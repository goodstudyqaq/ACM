void solve() {
    long long l, r;
    cin >> l >> r;
    long long now = 1;
    long long res = 0;
    int ans = 0;
    while (now < 1e9) {
        if (l <= now && now <= r) {
            ans = res;
        }
        now *= 2;
        res++;
    }
    cout << ans << endl;
}

