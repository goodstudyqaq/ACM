const int maxn = 1e5 + 5;
int a[maxn];
int sum[maxn], sum2[maxn];
int main() {
	freopen("data.in", "r", stdin);
	int n;
	scanf("%d", &n);
	for (int i = 1; i <= n; i++) {
		scanf("%d", &a[i]);
		sum[i] = sum[i - 1] + a[i];
	}
	if (sum[n] % 3) {
		puts("0");
		return 0;
	}
	int val = sum[n] / 3;
	vector<int>V;
	for (int i = n; i >= 1; i--) {
		sum2[i] = sum2[i + 1] + a[i];
		if (sum2[i] == val) {
			V.push_back(i);
		}
	}

	sort(V.begin(), V.end());
	int now = 0;
	long long ans = 0;
	for (int i = 1; i <= n; i++) {
		if (sum[i] == val) {
			while (now < V.size() && V[now] <= i + 1) {
				now++;
			}
			ans += V.size() - now;
		}
	}
	cout << ans << endl;
	return 0;
}