void solve() {
    int n;
    cin >> n;
    vector<ll> a(n), b(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
    }

    ll now_time = 0;

    for (int i = 0; i < n; i++) {
        ll start = a[i];
        now_time = max(now_time, start);
        ll end_time = b[i];
        cout << end_time - now_time << ' ';
        now_time = end_time;
    }
    cout << endl;
}

