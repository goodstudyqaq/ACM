void solve() {
    int n, x;
    cin >> n >> x;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    int now = 0;
    map<int, int> cnt;

    int idx = 0;
    while (true) {
        while (idx < n && a[idx] <= now) {
            cnt[a[idx] % x]++;
            idx++;
        }

        if (cnt[now % x] == 0) {
            cout << now << endl;
            return;
        }
        cnt[now % x]--;
        now++;
    }
}

