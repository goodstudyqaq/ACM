vector< long long > C;

pair< long long, long long > ans;

void dfs(long long val, long long a, long long b) {
	debug(val, a, b);
	if (val == 0) {
		ans = max(ans, { a, b });
		return;
	}
	int idx = upper_bound(C.begin(), C.end(), val) - C.begin();
	idx--;
	dfs(val - C[idx], a + 1, b + C[idx]);
	if (idx > 1) {
		dfs(C[idx] - 1 - C[idx - 1], a + 1, b + C[idx - 1]);
	}
}

