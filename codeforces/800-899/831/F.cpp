int ceil(int a, int b) {
	return (a - 1) / b + 1;
}

int main() {
#ifdef LOCAL
	freopen("data.in", "r", stdin);
#endif
	ios::sync_with_stdio(false);
	cin.tie(0);
	int n;
	long long k;
	cin >> n >> k;
	vector< int > a(n + 1);
	vector< long long > seg;
	for (int i = 1; i <= n; i++) {
		cin >> a[i];
		k += a[i];
		for (int j = 1; j * j <= a[i]; j++) {
			seg.push_back(j);
			seg.push_back(ceil(a[i], j));
		}
	}
	seg.push_back(1e12);
	sort(seg.begin(), seg.end());
	int scnt = unique(seg.begin(), seg.end()) - seg.begin();
	long long ans = 0;
	for (int i = 0; i < scnt - 1; i++) {
		long long l = seg[i], r = seg[i + 1];
		long long tmp = 0;
		for (int j = 1; j <= n; j++) {
			tmp += ceil(a[j], l);
		}
		long long d = k / tmp;
		if (d >= l && d < r && ans < d) {
			ans = d;
		}
	}
	cout << ans << endl;
	return 0;
}