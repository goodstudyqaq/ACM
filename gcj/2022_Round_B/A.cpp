void solve() {
    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    int nowl = 1, nowr = n;

    int mx = -1;
    int ans = 0;
    while (nowl <= nowr) {
        int tmp = min(a[nowl], a[nowr]);
        if (tmp == a[nowl]) {
            nowl++;
        } else {
            nowr--;
        }
        if (tmp >= mx) {
            ans++;
            mx = tmp;
        }
    }
    cout << ans << endl;
}

